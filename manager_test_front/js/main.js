function reloadPage(page){
    $('#page_title').html(page.replace(/^\w/, c => c.toUpperCase()));
    $('#page_content').html(spinner());
    $("#search_result").hide();
    window[page+"Page"](function(data){
        $('#page_content').html(data);
    });
}
